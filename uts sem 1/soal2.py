alas_segitiga = int(input("masukan alas segitiga : "))
tinggi_segitiga = int(input("masukan tinggi segitiga : "))
tinggi_limas = int(input("masukan tinggi limas : "))
volume_limas_segitga = (1/2*alas_segitiga*tinggi_segitiga)*tinggi_limas*1/3
print(f"volume limas segitiga yang dihasilkan adalah {volume_limas_segitga}")